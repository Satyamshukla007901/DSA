//A->https://codeforces.com/contest/1646/problem/A
void solve()
{
    ll N;
    ll S;
    cin >> N >> S;
    if (S == 0)
    {
        cout << 0 << endl;
    }
    else
    {
        cout << S / (N * N) << endl;
    }
}
